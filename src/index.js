const MORSE_TABLE = {
  ".-": "a",
  "-...": "b",
  "-.-.": "c",
  "-..": "d",
  ".": "e",
  "..-.": "f",
  "--.": "g",
  "....": "h",
  "..": "i",
  ".---": "j",
  "-.-": "k",
  ".-..": "l",
  "--": "m",
  "-.": "n",
  "---": "o",
  ".--.": "p",
  "--.-": "q",
  ".-.": "r",
  "...": "s",
  "-": "t",
  "..-": "u",
  "...-": "v",
  ".--": "w",
  "-..-": "x",
  "-.--": "y",
  "--..": "z",
  ".----": "1",
  "..---": "2",
  "...--": "3",
  "....-": "4",
  ".....": "5",
  "-....": "6",
  "--...": "7",
  "---..": "8",
  "----.": "9",
  "-----": "0",
};

function decode(expr) {
  return [...Array(expr.length / 10)]
    .map((_, i) => expr.slice(i * 10, i * 10 + 10))
    .map((el) => (letter(el) == undefined ? " " : letter(el)))
    .join("");
}
function letter(str) {
  return MORSE_TABLE[
    [...Array(5)]
      .map((_, i) => str.slice(i * 2, i * 2 + 2))
      .map((el) => (el == "10" ? "." : el == "11" ? "-" : ""))
      .join("")
  ];
}
